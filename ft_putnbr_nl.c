#include "so_long.h"

void	n_zero_min(int n)
{
	if (n == 0)
	{
		write(1, "0", 1);
	}
	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
	}
}

void	ft_putnbr_nl(int n)
{
	int	i;
	int	arr[10];

	i = 0;
	n_zero_min(n);
	if (n < 0 && n != -2147483648)
	{
		write(1, "-", 1);
		n = -n;
	}
	while (n > 0)
	{
		arr[i] = (n % 10) + 48;
		n = n / 10;
		i++;
	}
	while (i > 0)
	{
		i--;
		write(1, &arr[i], 1);
	}
	write (1, "\n", 1);
}
